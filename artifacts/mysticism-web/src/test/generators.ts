// arbMousePosition — Property 18 (tilt-card angle clamp)
// ---------------------------------------------------------------------------

/** Shape of a mouse position fixture generated by {@link arbMousePosition}. */
export interface ArbMousePosition {
  /** Cursor X within the card, in pixels: `[0, cardWidth]`. */
  x: number;
  /** Cursor Y within the card, in pixels: `[0, cardHeight]`. */
  y: number;
  /** Card width in pixels: `[100, 1200]`. */
  cardWidth: number;
  /** Card height in pixels: `[100, 1200]`. */
  cardHeight: number;
}

/**
 * Generates mouse positions inside a card's bounds.
 *
 * Card dimensions are picked first, then `x`/`y` are constrained to the
 * resulting rectangle. Used by Property 18 (tilt-card giới hạn độ nghiêng) —
 * see `design.md`.
 */
export const arbMousePosition: Arbitrary<ArbMousePosition> = fc
  .record({
    cardWidth: fc.integer({ min: 100, max: 1200 }),
    cardHeight: fc.integer({ min: 100, max: 1200 }),
  })
  .chain(({ cardWidth, cardHeight }) =>
    fc.record({
      cardWidth: fc.constant(cardWidth),
      cardHeight: fc.constant(cardHeight),
      x: fc.integer({ min: 0, max: cardWidth }),
      y: fc.integer({ min: 0, max: cardHeight }),
    }),
  );
