// ---------------------------------------------------------------------------
// arbRetryAfterHeader — Property 16 (parser Retry-After)
// ---------------------------------------------------------------------------

/** Shape returned by {@link arbRetryAfterHeader}. */
export interface ArbRetryAfter {
  /** Raw header value as it would arrive on the wire (or `null` if absent). */
  header: string | null;
  /** Parser's expected output in seconds; `0` for invalid / missing values. */
  expectedSeconds: number;
  /** Base timestamp (ms) used to build / interpret HTTP-date headers. */
  now: number;
}

/** Integer-seconds branch: a non-negative integer encoded as a base-10 string. */
const arbRetryAfterSeconds: Arbitrary<ArbRetryAfter> = fc
  .tuple(
    fc.integer({ min: 0, max: MAX_RETRY_AFTER_SECONDS }),
    fc.integer({ min: 0, max: 2_000_000_000_000 }),
  )
  .map(([seconds, now]) => ({
    header: String(seconds),
    expectedSeconds: seconds,
    now,
  }));

/** HTTP-date branch: `now + delta` formatted via `toUTCString`. */
const arbRetryAfterHttpDate: Arbitrary<ArbRetryAfter> = fc
  .tuple(
    fc.integer({ min: 0, max: MAX_RETRY_AFTER_SECONDS }),
    fc.integer({ min: 0, max: 2_000_000_000_000 }),
  )
  .map(([deltaSeconds, now]) => {
    const target = now + deltaSeconds * 1000;
    return {
      header: new Date(target).toUTCString(),
      // `toUTCString` truncates to whole seconds, so the round-trip is exact
      // when `now` is already on a second boundary; we floor to be safe.
      expectedSeconds: Math.max(
        0,
        Math.floor((new Date(target).getTime() - now) / 1000),
      ),
      now,
    };
  });

/** Garbage / unparseable string branch — parser must yield 0. */
const arbRetryAfterGarbage: Arbitrary<ArbRetryAfter> = fc
  .tuple(
    fc
      .string({ minLength: 1, maxLength: 30 })
      // Filter out anything that could legitimately parse as a non-negative
      // integer or a valid Date, so we keep this branch firmly invalid.
      .filter((s) => {
        const trimmed = s.trim();
        if (trimmed.length === 0) return false;
        if (/^\d+$/.test(trimmed)) return false;
        const parsed = Date.parse(trimmed);
        return Number.isNaN(parsed);
      }),
    fc.integer({ min: 0, max: 2_000_000_000_000 }),
  )
  .map(([garbage, now]) => ({
    header: garbage,
    expectedSeconds: 0,
    now,
  }));

/** Missing-header branch — `null` must yield 0. */
const arbRetryAfterNull: Arbitrary<ArbRetryAfter> = fc
  .integer({ min: 0, max: 2_000_000_000_000 })
  .map((now) => ({ header: null, expectedSeconds: 0, now }));

/**
 * Generates a Retry-After header fixture: integer seconds, HTTP-date, garbage,
 * or `null`. The `expectedSeconds` field is the parser's expected output.
 *
 * Used by Property 16 (parser Retry-After) — see `design.md`.
 */
export const arbRetryAfterHeader: Arbitrary<ArbRetryAfter> = fc.oneof(
  arbRetryAfterSeconds,
  arbRetryAfterHttpDate,
  arbRetryAfterGarbage,
  arbRetryAfterNull,
);

